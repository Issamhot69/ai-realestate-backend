const PDFDocument = require('pdfkit');
const Property = require('../properties/property.model');

const generateReport = async (req, res) => {
  try {
    const { propertyId } = req.params;
    const property = await Property.findById(propertyId).populate('agent', 'name email');

    if (!property) return res.status(404).json({ message: 'Property not found' });

    const doc = new PDFDocument({ margin: 50 });
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', 'attachment; filename=investment-report.pdf');
    doc.pipe(res);

    // Header
    doc.rect(0, 0, 612, 100).fill('#1e3a5f');
    doc.fillColor('white').fontSize(26).font('Helvetica-Bold')
      .text('AI REALESTATE', 50, 30);
    doc.fontSize(14).font('Helvetica')
      .text('Investment Analysis Report', 50, 65);
    doc.fillColor('black');
    doc.moveDown(4);

    // Property Info
    doc.fontSize(18).font('Helvetica-Bold').fillColor('#1e3a5f').text('PROPERTY OVERVIEW');
    doc.moveDown();
    doc.fontSize(12).font('Helvetica').fillColor('black');
    doc.text('Title: ' + property.title);
    doc.text('Category: ' + property.category);
    doc.text('Location: ' + property.location?.city + ', ' + property.location?.country);
    doc.text('Address: ' + (property.location?.address || 'N/A'));
    doc.text('Surface: ' + (property.surface || 'N/A') + ' m2');
    doc.text('Rooms: ' + (property.rooms || 'N/A'));
    doc.text('Bathrooms: ' + (property.bathrooms || 'N/A'));
    doc.text('Furnished: ' + (property.furnished ? 'Yes' : 'No'));
    doc.text('Status: ' + property.status);
    doc.moveDown(2);

    // Financial Analysis
    doc.fontSize(18).font('Helvetica-Bold').fillColor('#1e3a5f').text('FINANCIAL ANALYSIS');
    doc.moveDown();
    doc.fontSize(12).font('Helvetica').fillColor('black');
    doc.text('Sale Price: ' + property.price?.toLocaleString() + ' EUR');

    const monthlyRent = property.price * (property.roiScore / 100) / 12;
    const annualReturn = property.price * (property.roiScore / 100);

    doc.text('Estimated Monthly Rent: ' + monthlyRent.toFixed(0) + ' EUR');
    doc.text('Annual Return: ' + annualReturn.toFixed(0) + ' EUR');
    doc.moveDown(2);

    // AI Scores
    doc.fontSize(18).font('Helvetica-Bold').fillColor('#1e3a5f').text('AI SCORES');
    doc.moveDown();
    doc.fontSize(12).font('Helvetica').fillColor('black');

    // ROI Bar
    doc.text('ROI Score: ' + property.roiScore + '%');
    doc.rect(50, doc.y + 5, 400, 20).fill('#e5e7eb');
    doc.rect(50, doc.y - 15, (400 * property.roiScore) / 20, 20).fill('#16a34a');
    doc.fillColor('black');
    doc.moveDown(2);

    // Risk Bar
    doc.text('Risk Score: ' + property.riskScore + '/100');
    doc.rect(50, doc.y + 5, 400, 20).fill('#e5e7eb');
    const riskColor = property.riskScore < 30 ? '#16a34a' : property.riskScore < 60 ? '#f59e0b' : '#dc2626';
    doc.rect(50, doc.y - 15, (400 * property.riskScore) / 100, 20).fill(riskColor);
    doc.fillColor('black');
    doc.moveDown(2);

    // Investment Recommendation
    doc.fontSize(18).font('Helvetica-Bold').fillColor('#1e3a5f').text('AI RECOMMENDATION');
    doc.moveDown();
    doc.fontSize(12).font('Helvetica').fillColor('black');

    let recommendation = '';
    if (property.roiScore >= 10 && property.riskScore < 40) {
      recommendation = 'STRONG BUY — High ROI with low risk. Excellent investment opportunity.';
    } else if (property.roiScore >= 7 && property.riskScore < 60) {
      recommendation = 'BUY — Good ROI with moderate risk. Recommended for diversified portfolios.';
    } else if (property.roiScore >= 5) {
      recommendation = 'HOLD — Average ROI. Consider negotiating price for better returns.';
    } else {
      recommendation = 'CAUTION — Low ROI. Thorough due diligence recommended before investing.';
    }

    doc.fontSize(14).font('Helvetica-Bold').text(recommendation);
    doc.moveDown(2);

    // 5 Year Projection
    doc.fontSize(18).font('Helvetica-Bold').fillColor('#1e3a5f').text('5 YEAR PROJECTION');
    doc.moveDown();
    doc.fontSize(12).font('Helvetica').fillColor('black');

    for (let year = 1; year <= 5; year++) {
      const projection = property.price + (annualReturn * year);
      doc.text('Year ' + year + ': ' + projection.toFixed(0) + ' EUR (Total Value)');
    }
    doc.moveDown(2);

    // Agent Info
    doc.fontSize(18).font('Helvetica-Bold').fillColor('#1e3a5f').text('CONTACT');
    doc.moveDown();
    doc.fontSize(12).font('Helvetica').fillColor('black');
    doc.text('Agent: ' + (property.agent?.name || 'N/A'));
    doc.text('Email: ' + (property.agent?.email || 'N/A'));
    doc.moveDown(2);

    // Footer
    doc.fontSize(10).fillColor('#6b7280')
      .text('Generated by AI RealEstate — ' + new Date().toLocaleDateString(), { align: 'center' });

    doc.end();
  } catch (err) {
    res.status(500).json({ message: 'Error generating report', error: err.message });
  }
};

module.exports = { generateReport };